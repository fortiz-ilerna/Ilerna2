// Ejercicio 2: Uso de Constantes
/*Declara una constante llamada GRAVEDAD 
con el valor de 9.8. Calcula y muestra la fuerza 
de un objeto de masa m = 10 kg utilizando esta 
constante (Fuerza = masa × gravedad).*/

// Declarar la constante de gravedad
const GRAVEDAD = 9.8; // m/s²

// Declarar la masa del objeto
let masa = 10; // kg

// Calcular la fuerza
let fuerza = masa * GRAVEDAD;

// Mostrar el resultado por pantalla
console.log("La fuerza del objeto es:", fuerza, "N"); // N = Newtons